public class Swapping {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int temp;
        temp = a;
        a = b;
        b = temp;
        System.out.println("Value of A after swapping: " + a);
        System.out.println("Value of B after swapping: " + b);
 
    }
   }
