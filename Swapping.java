public class Swapping {
    public static void main(String[] args) {
 int a=3;
 System.out.println("Here the value of a is: "+a);
 int b=45;
 System.out.println("Here the value of b is: "+b);
 int temp;

 temp = a;
 System.out.println("Here the value of temp is:" +temp);
 //here we are swapping the values.
 a = b;
 System.out.println("Here the value of 'a' after the swapping: "+a);
 //swappping the value of b
 b=temp;
 System.out.println("Here the value of 'b' after the swapping: "+b);

    }
   }
